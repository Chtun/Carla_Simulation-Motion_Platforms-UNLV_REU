class SensorControl(object):
    """Class that handles keyboard input for the sensor"""
    def __init__(self, world):
        if isinstance(world.sensor_control, carla.Sensor):
            self._velocity = carla.Vector3D()
            self._angular_velocity = carla.Vector3D()
        else:
            raise NotImplementedError("Actor type not supported")
        world.hud.notification("Press 'H' or '?' for help.", seconds=4.0)


    def parse_events(self, client, world, clock, sync_mode):
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                return True
            elif event.type == pygame.KEYUP:
                if self._is_quit_shortcut(event.key):
                    return True
                elif event.key == K_BACKSPACE:
                    world.restart()
                elif event.key == K_F1:
                    world.hud.toggle_info()
                elif event.key == K_v and pygame.key.get_mods() & KMOD_SHIFT:
                    world.next_map_layer(reverse=True)
                elif event.key == K_v:
                    world.next_map_layer()
                elif event.key == K_b and pygame.key.get_mods() & KMOD_SHIFT:
                    world.load_map_layer(unload=True)
                elif event.key == K_b:
                    world.load_map_layer()
                elif event.key == K_h or (event.key == K_SLASH and pygame.key.get_mods() & KMOD_SHIFT):
                    world.hud.help.toggle()
                elif event.key == K_TAB:
                    world.sensor_manager.toggle_camera()
                elif event.key == K_c and pygame.key.get_mods() & KMOD_SHIFT:
                    world.next_weather(reverse=True)
                elif event.key == K_c:
                    world.next_weather()
                elif event.key == K_g:
                    world.toggle_radar()
                elif event.key == K_BACKQUOTE:
                    world.sensor_manager.next_sensor()
                elif event.key == K_n:
                    world.sensor_manager.next_sensor()
                elif event.key > K_0 and event.key <= K_9:
                    index_ctrl = 0
                    if pygame.key.get_mods() & KMOD_CTRL:
                        index_ctrl = 9
                    world.sensor_manager.set_sensor(event.key - 1 - K_0 + index_ctrl)
                elif event.key == K_r and not (pygame.key.get_mods() & KMOD_CTRL):
                    world.sensor_manager.toggle_recording()
                elif event.key == K_r and (pygame.key.get_mods() & KMOD_CTRL):
                    if (world.recording_enabled):
                        client.stop_recorder()
                        world.recording_enabled = False
                        world.hud.notification("Recorder is OFF")
                    else:
                        client.start_recorder("manual_recording.rec")
                        world.recording_enabled = True
                        world.hud.notification("Recorder is ON")
                elif event.key == K_p and (pygame.key.get_mods() & KMOD_CTRL):
                    # stop recorder
                    client.stop_recorder()
                    world.recording_enabled = False
                    # work around to fix camera at start of replaying
                    current_index = world.sensor_manager.index
                    world.destroy_sensors()
                    world.hud.notification("Replaying file 'manual_recording.rec'")
                    # replayer
                    client.replay_file("manual_recording.rec", world.recording_start, 0, 0)
                    world.sensor_manager.set_sensor(current_index)
                elif event.key == K_MINUS and (pygame.key.get_mods() & KMOD_CTRL):
                    if pygame.key.get_mods() & KMOD_SHIFT:
                        world.recording_start -= 10
                    else:
                        world.recording_start -= 1
                    world.hud.notification("Recording start time is %d" % (world.recording_start))
                elif event.key == K_EQUALS and (pygame.key.get_mods() & KMOD_CTRL):
                    if pygame.key.get_mods() & KMOD_SHIFT:
                        world.recording_start += 10
                    else:
                        world.recording_start += 1
                    world.hud.notification("Recording start time is %d" % (world.recording_start))
                print("Finished parsing events")

        if world.sensor_control:
            self._parse_movement_keys(pygame.key.get_pressed())
            world.sensor_control.set_target_velocity(self._velocity)
            world.sensor_control.set_target_angular_velocity(self._angular_velocity)
            if self._velocity.length() != 0:
                print("velocity -\nx: ", str(self._velocity.x), "\ny: ", str(self._velocity.y), "\nz: ", str(self._velocity.z))

    def _parse_movement_keys(self, keys):
        velocity = carla.Vector3D(0, 0, 0)
        angular_velocity = carla.Vector3D(0, 0, 0)
        if keys[K_UP] or keys[K_w]:
            velocity.x += 5
            print("Forward key being pressed!")

        if keys[K_DOWN] or keys[K_s]:
            velocity.x -= 5
            print("Backwards key being pressed!")

        if keys[K_LEFT] or keys[K_a]:
            angular_velocity.z += 5
            print("Leftwards key being pressed!")
        
        if keys[K_RIGHT] or keys[K_d]:
            angular_velocity.z += 5
            print("Rightwards key being pressed!")

        self._velocity = velocity
        self._angular_velocity = angular_velocity
    

    @staticmethod
    def _is_quit_shortcut(key):
        return (key == K_ESCAPE) or (key == K_q and pygame.key.get_mods() & KMOD_CTRL)

