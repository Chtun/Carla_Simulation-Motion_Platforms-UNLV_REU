def find_weather_presets():
    rgx = re.compile('.+?(?:(?<=[a-z])(?=[A-Z])|(?<=[A-Z])(?=[A-Z][a-z])|$)')
    name = lambda x: ' '.join(m.group(0) for m in rgx.finditer(x))
    presets = [x for x in dir(carla.WeatherParameters) if re.match('[A-Z].+', x)]
    return [(getattr(carla.WeatherParameters, x), name(x)) for x in presets]

def get_actor_display_name(actor, truncate=250):
    name = ' '.join(actor.type_id.replace('_', '.').title().split('.')[1:])
    return (name[:truncate - 1] + u'\u2026') if len(name) > truncate else name

def get_sensor_blueprint(world, choice="camera_rgb"):
    
    # Default sensor is sensor.camera.rgb -> rgb camera
    sensor_choice = ""
    if choice == "camera_rgb":
        sensor_choice = "sensor.camera.rgb"
    elif choice == "camera_depth":
        sensor_choice = "sensor.camera.depth"
    elif choice == "camera_segmentation":
        sensor_choice = "sensor.camera.semantic_segmentation"
    elif choice == "lidar":
        sensor_choice = "sensor.lidar.ray_cast"
    elif choice == "lidar_semantic":
        sensor_choice = "sensor.lidar.ray_cast_semantic"
    elif choice == "radar":
        sensor_choice = "sensor.other.radar"
    else:
        print("The selected sensor is not valid. Please Choose from one of the following sensors:\n'camera_rgb'\n'camera_depth'\n'camera_segmentation'\n'lidar'\n'lidar_semantic'\n'radar'\n")
        exit(1)

    bp = world.get_blueprint_library().find(sensor_choice)

    for attr in bp:
        print('  - {}'.format(attr))

    return bp
    


